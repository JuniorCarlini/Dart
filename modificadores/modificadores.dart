void main() {
  //final (depois de criada não pode ser alterada)
  //ultilizar sem moderação
  final nomeCompleto = "Junior Carlini";

  //const (depois de criada não pode ser alterada, mas ela deve ser definida em momento de compilação )
  //não podem receber valores de outras variaveis a não ser que essas variaveis forem const tembem
  const nomeCompletoConst = "Junior Carlini";


  print(nomeCompleto);
  print(nomeCompletoConst);
}
